    /**
     * Metodo para caragar tabla cliente
     */
    private void CargarTablaAsignaciones() {
        Connection con = Conexion.conectar();
        DefaultTableModel model = new DefaultTableModel();
        String sql = "select * from tb_asignacion";
        Statement st;

        try {
            st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            InterGestionarAsignacion.jTable_asignaciones = new JTable(model);
            InterGestionarAsignacion.jScrollPane1.setViewportView(InterGestionarAsignacion.jTable_asignaciones);

            model.addColumn("N¬∞");
            model.addColumn("CODIGO OPERACION");
            model.addColumn("DESCRIPCION");
            model.addColumn("SUPERVISOR");
            model.addColumn("EMPLEADO");
            model.addColumn("FECHA INICIO");
            model.addColumn("FECHA FIN");
            model.addColumn("EVALUACION");

            while (rs.next()) {
                Object fila[] = new Object[8];
                for (int i = 0; i < 8; i++) {
                    fila[i] = rs.getObject(i + 1);
                }
                model.addRow(fila);

            }
            con.close();

        } catch (SQLException e) {
            System.out.println("Error al llenar la tabla asignacion" + e);
        }

        jTable_asignaciones.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                int fila_point = jTable_asignaciones.rowAtPoint(e.getPoint());
                int columna_point = 0;

                if (fila_point > -1) {
                    idAsignacion = (int) model.getValueAt(fila_point, columna_point);
                    EnviarDatosAsignacionSeleccionado(idAsignacion);//metodo
                }
            }
        });

    }

    /**
     * Metodos que envia datos seleccionados de la tabla gestionar cliente
     */
    private void EnviarDatosAsignacionSeleccionado(int idAsignacion) {

        try {

            Connection con = Conexion.conectar();
            PreparedStatement pst1 = con.prepareStatement("select * from tb_asignacion where idAsignacion = '" + idAsignacion + "'");
            ResultSet rs = pst1.executeQuery();

            if (rs.next()) {
                jTextField_idAsignacion.setText(Integer.toString(idAsignacion));
                jTextArea_evaluacion.setText(rs.getString("evaluacion"));
                int idEmpleado = rs.getInt("idEmpleado");
                int idSupervisor = rs.getInt("idSupervisor");

                PreparedStatement pst3 = con.prepareStatement("select nombre, apellido from tb_usuario where idUsuario= '" + idEmpleado + "'");
                ResultSet rs3 = pst3.executeQuery();
                if (rs3.next()) {
                    jTextField_empleado.setText( rs3.getString("nombre")+" "+ rs3.getString("apellido"));
                }
                rs3.close();

                PreparedStatement pst4 = con.prepareStatement("select nombre, apellido from tb_usuario where idUsuario= '" + idSupervisor + "'");
                ResultSet rs4 = pst4.executeQuery();
                if (rs4.next()) {
                    jTextField_supervisor.setText(rs4.getString("nombre")+" "+ rs4.getString("apellido"));
                }
                rs4.close();
            }
            rs.close();
            con.close();
        } catch (SQLException e) {
            System.out.println("Error Seleccionar Asignacion");
        }
    }

