    private void jButton_GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_GuardarActionPerformed
        if (txt_nombre.getText().isEmpty() | txt_apellido.getText().isEmpty() | txt_usuario.getText().isEmpty()
                | txt_password.getText().isEmpty() | txt_telefono.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Complete todos los campos");
        } else {
            //Validar si usuario esta registrado
            Usuario usuario = new Usuario();

            Ctrl_Usuario controlUsuario = new Ctrl_Usuario();
            if (!controlUsuario.existeUsuario(txt_usuario.getText().trim())) {
                usuario.setNombre(txt_nombre.getText().trim());
                usuario.setApellido(txt_apellido.getText().trim());
                usuario.setRut(txt_rut.getText().trim());
                usuario.setUsuario(txt_usuario.getText().trim());
                usuario.setPassword(txt_password.getText().trim());
                usuario.setCargo(txt_cargo.getText().trim());
                usuario.setTelefono(txt_telefono.getText().trim());
                usuario.setCorreoElectronico(txt_correoElectronico.getText().trim());
                usuario.setDireccion(txt_direccion.getText().trim());
                usuario.setEstado(1);
               // Obtener fecha de ingreso
                Date fechaSeleccionadaI = jDateChooser_fechaIngreso.getDate();
                if (fechaSeleccionadaI != null) {
                    SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                    String fechaComoString = sdf.format(fechaSeleccionadaI);
                    usuario.setFechaIngreso(fechaComoString);
                }
                
                // No establecer fecha de egreso
                usuario.setFechaEgreso(null);

                if (controlUsuario.guardar(usuario)) {
                    JOptionPane.showMessageDialog(null, "Usuario Guardado");
                } else {
                    JOptionPane.showMessageDialog(null, "Error al registrar Usuario");
                }
                this.LimpiarCampos();
                

                

            } else {
                JOptionPane.showMessageDialog(null, "Usuario ya esta registrado, ingrese nuevo usuario");
            }
        }

    }//GEN-LAST:event_jButton_GuardarActionPerformed

