    private void Checkb_verclaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Checkb_verclaveMouseClicked
        // TODO add your handling code here:

        if (Checkb_verclave.isSelected() == true) {

            char[] passwordIngresado = txt_password.getPassword();

            txt_password_visible.setText(new String(passwordIngresado));
            txt_password.setVisible(false);
            txt_password_visible.setVisible(true);
        } else {
            String passwIngresado = txt_password_visible.getText().trim();
            txt_password.setText(passwIngresado);
            txt_password.setVisible(true);
            txt_password_visible.setVisible(false);

        }
    }//GEN-LAST:event_Checkb_verclaveMouseClicked

