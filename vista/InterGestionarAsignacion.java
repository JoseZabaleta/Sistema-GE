    private void jButton_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_actualizarActionPerformed
        if (jTextArea_evaluacion.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Realizar evaluaci√≥n");
        } else {
            Asignacion asignacion = new Asignacion();
            Ctrl_Asignacion controlAsignacion = new Ctrl_Asignacion();
            asignacion.setEvaluacion(jTextArea_evaluacion.getText());

            if (controlAsignacion.evaluar(asignacion, idAsignacion)) {
                JOptionPane.showMessageDialog(null, "Asignacion evaluada");
                this.CargarTablaAsignaciones();
                this.Limpiar();
            } else {
                JOptionPane.showMessageDialog(null, "Error al evaluar");
            }
        }

    }//GEN-LAST:event_jButton_actualizarActionPerformed

