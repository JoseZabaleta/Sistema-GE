    private void jButton_GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_GuardarActionPerformed
        // TODO add your handling code here:
        String departamentoSeleccion = "";
        departamentoSeleccion = jComboBox_departamento.getSelectedItem().toString().trim();
        Operacion operacion = new Operacion();
        Ctrl_Operacion controlOperacion = new Ctrl_Operacion();
        if (!txt_descripcion.getText().isEmpty() && !txt_tiempoEjecuccion.getText().isEmpty() && !jComboBox_departamento.getSelectedItem().equals("Seleccione departamento:")) {
            //JOptionPane.showMessageDialog(null,"Correcto");
            if (!controlOperacion.existeOperacion(txt_descripcion.getText().trim())) {
                operacion.setDescripcion(txt_descripcion.getText().trim());
                operacion.setTiempoEjecuccion(Integer.parseInt(txt_tiempoEjecuccion.getText().trim()));
                operacion.setDepartamento((departamentoSeleccion));

                if (controlOperacion.guardar(operacion)) {
                    JOptionPane.showMessageDialog(null, "Registro Exitoso");
                } else {
                    JOptionPane.showMessageDialog(null, "Error al guardar");
                }

            } else {
                JOptionPane.showMessageDialog(null, "La operacion existe en la base de datos");
            }

        } else {
            JOptionPane.showMessageDialog(null, "Completa todos los campos");
        }
        // Metodo limpiar
        this.LimpiarCampos();


    }//GEN-LAST:event_jButton_GuardarActionPerformed


